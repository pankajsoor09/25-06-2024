package com.javaExpressions;

public class Example1
{

	public static void main(String[] args) 
	{
		
		int firstValue = 300;
		int secondValue = 200;
		
		int result = firstValue*secondValue;
		/**
		 * result = firstValue*secondValue => an expression
		 * 
		 * from the above expression,
		 * 
		 * firstValue, secondValue and result are operands(Variables and Constants) 
		 * 
		 * = and * are operators
		 * 
		 * 
		 * operands + operators = expression
		 * 
		 * 
		 */
		
		
		System.out.println(result);
		
	}

}
